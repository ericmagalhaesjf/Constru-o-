public class Cliente {
    String nome;
    int dataDeNacimento;
    int cpf;
    int telefone;

    public Cliente(String nome, int dataDeNacimento, int cpf, int telefone) {
        this.nome = nome;
        this.dataDeNacimento = dataDeNacimento;
        this.cpf = cpf;
        this.telefone = telefone;
    }
}
